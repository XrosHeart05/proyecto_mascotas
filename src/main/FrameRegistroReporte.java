  private void botonGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGuardarActionPerformed
    // Invocar método de guardar reporte
    Reporte reporte;
    Mascota mascota = null;
    List<String> especiesPermitidas = Arrays.asList("DOG", "CAT");
    List<String> pelajesPermitidos = Arrays.asList("CORTO", "LARGO");
    List<String> opcionesSiNo = Arrays.asList("Si", "No");

    // Validaciones
    String reporteID = InputController.inputReporteIdInterfaz(textoReporteId.getText());
    if (reporteID == null) {
      return;
    }
    if (!reporteController.existeId(reporteID)) {
      JOptionPane.showMessageDialog(null, "El ID de reporte " + reporteID + " ya existe, digite otro");
      return;
    }
    
    String cedula = InputController.inputIDInterfaz(textoCedula.getText());
    if (cedula == null) {
      return;
    }
    
    String nombre = InputController.inputStringMinimoInterfaz(7, textoNombre.getText(), "Nombre");
    if (nombre == null) {
      return;
    }
    
    String fecha = InputController.inputFechaInterfaz(textoFecha.getText());
    if (fecha == null) {
      return;
    }
    
    String zona = InputController.inputStringRangoInterfaz(textoZona.getText(), 1, 30, "Zona");
    if (zona == null) {
      return;
    }
    
    String contacto = InputController.inputTelefonoInterfaz(textoContacto.getText());
    if (contacto == null) {
      return;
    }
    
    String especie = InputController.inputRestrictInterfaz(especiesPermitidas, comboEspecie.getSelectedItem().toString());
    if (especie == null) {
      return;
    }
    
    String color = InputController.inputStringInterfaz(textoColor.getText(), "Color");
    if (color == null) {
      return;
    }
    
    String sennas = InputController.inputStringRangoInterfaz(textoSennas.getText(), 10, 100, "Señas");
    if (sennas == null) {
      return;
    }

    // No necesita validación
    String microchip = textoMicrochip.getText();

    // Crear mascota
    if ("CAT".equals(especie)) {
      String razaGato = InputController.inputStringInterfaz(textoRazaGato.getText(), "Raza del gato");
      if (razaGato == null) {
        return;
      }
      
      String tipoPelajeGato = InputController.inputRestrictInterfaz(pelajesPermitidos, comboTipoPelajeGato.getSelectedItem().toString());
      if (tipoPelajeGato == null) {
        return;
      }
      
      String esterelizadoGato = InputController.inputRestrictInterfaz(opcionesSiNo, comboEsterelizadoGato.getSelectedItem().toString());
      boolean esterelizadoGatoB = false;
      if (esterelizadoGato == null) {
        return;
      }
      
      if ("Si".equals(esterelizadoGato)) {
        esterelizadoGatoB = true;
      }
      mascota = new Gato(razaGato, tipoPelajeGato, esterelizadoGatoB, especie, color, sennas, microchip);
    }
    if ("DOG".equals(especie)) {
      
      String razaPerro = InputController.inputStringInterfaz(textoRazaPerro.getText(), "Raza del perro");
      if (razaPerro == null) {
        return;
      }
      
      List<String> tallasPermitidasPerro = Arrays.asList("PEQ", "MED", "GRA");
      String tallaPerro = InputController.inputRestrictInterfaz(tallasPermitidasPerro, comboTallaPerro.getSelectedItem().toString());
      if (tallaPerro == null) {
        return;
      }
      
      String collarPerro = InputController.inputRestrictInterfaz(opcionesSiNo, comboCollarPerro.getSelectedItem().toString());
      boolean collarPerroB = false;
      if (collarPerro == null) {
        return;
      }
      
      if ("Si".equals(collarPerro)) {
        collarPerroB = true;
      }
      mascota = new Perro(razaPerro, tallaPerro, collarPerroB, especie, color, sennas, microchip);
    }
    
    if ("PDR".equals(comboTipoReporte.getSelectedItem().toString())) {
      reporte = new ReportePerdida(reporteID, cedula, nombre, fecha, zona, contacto, mascota);
    } else {
      reporte = new ReporteEncontrada(reporteID, cedula, nombre, fecha, zona, contacto, mascota);
    }
    
    try {
      reporteController.crearReporte(reporte);
      JOptionPane.showMessageDialog(null, "Reporte guardado exitosamente");
      limpiarDatos();
    } catch (HeadlessException e) {
      JOptionPane.showMessageDialog(null, "Error al agregar el reporte. Error: " + e.getMessage());
    }
  }//GEN-LAST:event_botonGuardarActionPerformed
  
