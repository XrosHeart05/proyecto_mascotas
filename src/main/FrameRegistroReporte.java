    private void comboEspecieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboEspecieActionPerformed
      // Mostrar paneles respecto a especie escogida
      String especieSeleccionada = comboEspecie.getSelectedItem().toString();
      
      if (especieSeleccionada.equals("CAT")) {
        panelGato.setVisible(true);
        panelPerro.setVisible(false);
      } else if (especieSeleccionada.equals("DOG")) {
        panelPerro.setVisible(true);
        panelGato.setVisible(false);
      } else {
        panelGato.setVisible(false);
        panelPerro.setVisible(false);
      }

      // Actualiza la interfaz
      panelPrincipal.revalidate();
      panelPrincipal.repaint();
    }//GEN-LAST:event_comboEspecieActionPerformed

