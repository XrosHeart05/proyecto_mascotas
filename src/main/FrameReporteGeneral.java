    private void MostrarDatos() {
        try (BufferedReader lecDatos = new BufferedReader(new FileReader("reportes_perdidas.txt"))) {
            String linea;
            while ((linea = lecDatos.readLine()) != null) {
                String[] columns = linea.split("\\|");

                String[] row = {columns[0], columns[2], columns[3], columns[4],"PDR", columns[6]};
                
                table.addRow(row);
            }
            
        } catch (IOException e) {
            System.out.println("Error: no se pudo completar la funci√≥n");
        }
    }

